package vn.edu.poly.testduan2.model;

public class MilkTea {

    private int imgMilk;
    private String title;

    public MilkTea(int imgMilk, String title) {
        this.imgMilk = imgMilk;
        this.title = title;
    }

    public int getImgMilk() {
        return imgMilk;
    }

    public void setImgMilk(int imgMilk) {
        this.imgMilk = imgMilk;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
